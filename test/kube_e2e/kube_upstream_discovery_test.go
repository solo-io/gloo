package kube_e2e

import (
	"time"

	. "github.com/onsi/ginkgo"
	"github.com/pborman/uuid"
	"github.com/solo-io/gloo/pkg/api/types/v1"
	. "github.com/solo-io/gloo/test/helpers"
)

var _ = Describe("Kube Upstream Discovery", func() {
	generatedUpstreamName := namespace + "-helloservice-8080"
	Context("creating a vService with a route to an upstream generated by"+
		" kube-upstream-discovery", func() {
		randomPath := "/" + uuid.New()
		vServiceName := "one-route"
		BeforeEach(func() {
			_, err := gloo.V1().VirtualServices().Create(&v1.VirtualService{
				Name: vServiceName,
				Routes: []*v1.Route{{
					Matcher: &v1.Route_RequestMatcher{
						RequestMatcher: &v1.RequestMatcher{
							Path: &v1.RequestMatcher_PathExact{
								PathExact: randomPath,
							},
							Verbs: []string{"GET"},
						},
					},
					SingleDestination: &v1.Destination{
						DestinationType: &v1.Destination_Upstream{
							Upstream: &v1.UpstreamDestination{
								Name: generatedUpstreamName,
							},
						},
					},
				}},
			})
			Must(err)
		})
		AfterEach(func() {
			//gloo.V1().Upstreams().Delete(generatedUpstreamName)
			gloo.V1().VirtualServices().Delete(vServiceName)
		})
		It("should configure envoy with a 200 OK route (backed by helloservice)", func() {
			curlEventuallyShouldRespond(curlOpts{path: randomPath}, "< HTTP/1.1 200", time.Minute*5)
		})
	})
})
